appendix = """

## 十一、二次核查补漏

这一节是对照标黄课件、两份习题解答和本地复习笔记后补上的漏项。前面章节已经覆盖主干，但以下内容容易在单选、简答或应用题里被单独拎出来。

### 1. 字符与校验码

- ASCII 通常是 7 位编码，可表示 128 个字符。
- 汉字编码可记：GB2312、GBK、Unicode、UTF-8、UTF-16。选择题常问“字符集”和“编码方式”区别，Unicode 是统一字符集，UTF-8/UTF-16 是编码实现。
- 奇偶校验：增加 1 位校验位，使 1 的个数满足奇数或偶数。能发现奇数个比特错误，尤其单比特错误；不能定位错误，也不能纠错，偶数个错误可能漏检。

### 2. SRAM、DRAM、ROM

- SRAM：速度快，不需刷新，常用于 Cache；集成度低、成本高。
- DRAM：容量大、成本低，需要刷新，常用于主存。
- ROM：掉电不丢失，主要用于读；常见分类按教材记 ROM、PROM、EPROM、EEPROM/Flash。
- DRAM 刷新方式：集中刷新、分散刷新、异步刷新。计算题重点仍是刷新间隔、集中刷新总时间、死时间率。

### 3. Cache 写策略

命中时：

- 写直达/全写法：写 Cache 的同时写主存。优点是主存总是新数据，缺点是写慢。
- 写回法：先只写 Cache，替换时再写回主存。优点是减少主存写次数，缺点是要脏位，控制复杂。

未命中时：

- 写分配：先把块调入 Cache，再写。
- 非写分配：不调入 Cache，直接写主存。

### 4. 页式、段式、段页式比较

- 页式：页大小固定，地址为页号 + 页内地址。优点是无外部碎片、管理方便；缺点是有内部碎片，页表可能很大。
- 段式：地址为段号 + 段内地址，段表含段基址和段长。优点是符合程序逻辑，便于共享和保护；缺点是有外部碎片。
- 段页式：先分段、段内分页，地址为段号 + 页号 + 页内地址。兼有逻辑性和固定分页管理，但地址转换更复杂。

### 5. 原码/补码乘除与 ALU 进位

原码阵列乘法：

1. 符号位单独处理，结果符号为两个符号位异或。
2. 数值位按无符号乘法计算。
3. 拼上符号位得到原码乘积。

补码阵列乘法：

1. 先看操作数符号，负数需要算前求补得到绝对值。
2. 数值位按无符号乘法。
3. 若乘积应为负，算后再求补。

加减交替除法：

- 先把被除数、除数按题意变成定点小数。
- 余数为正时减除数，余数为负时加除数。
- 每一步左移、判余数符号、确定商位。

ALU 先行进位常用式：

```text
Gi = AiBi
Pi = Ai + Bi
Ci = Gi + Pi*C(i-1)
C1 = G1 + P1C0
C2 = G2 + P2G1 + P2P1C0
C3 = G3 + P3G2 + P3P2G1 + P3P2P1C0
C4 = G4 + P4G3 + P4P3G2 + P4P3P2G1 + P4P3P2P1C0
```

### 6. 微指令编码与相斥/相容微操作

- 水平型微指令：并行性强、速度快，但字长长。
- 垂直型微指令：编码短、节省控存，但译码多、速度较慢。
- 直接控制法：一个微命令对应一位，快但费位。
- 字段译码法：把互斥微命令放进同一字段，节省位数，但同字段内不能并行。
- 字段位数：若一组有 n 个互斥微命令，还要留“无操作”编码，则 `2^k >= n + 1`。
- 控存有 `2^k` 个单元时，后继微地址字段至少 k 位。
- 相斥微操作：同一时刻不能同时发生，例如同一总线多个部件同时输出。
- 相容微操作：可在同一节拍并行发生，例如不争用同一资源的寄存器打入和运算控制。

### 7. CISC 与 RISC

- CISC：指令多、格式复杂、寻址方式多、执行时间不固定，控制器复杂。
- RISC：指令少、格式规整、寻址方式少，多数指令周期短，Load/Store 结构，寄存器多，适合流水线，更多依赖编译器优化。

### 8. 总线与串行通信

- 总线按信息分：地址总线、数据总线、控制总线。
- 总线按位置分：片内总线、系统总线、外部总线。
- 总线仲裁：集中式、分布式。
- 总线定时：同步、异步、半同步/分离式。
- 异步串行一帧通常是：起始位 + 数据位 + 校验位 + 停止位。字符传输率一定要用波特率除以“一帧总位数”。

### 9. 显示设备

显示类题目重点不多，但单选/小计算可能出现：

```text
显存容量 = 水平像素数 * 垂直像素数 * 每像素位数
显示带宽 = 显存容量 * 刷新率
```

概念：分辨率、像素、颜色深度、刷新率、显存容量。

### 10. 中断与 DMA 细节

中断过程：

```text
中断请求 -> 中断判优 -> 中断响应 -> 保存现场 -> 执行中断服务程序 -> 恢复现场 -> 中断返回
```

中断方式与 DMA 区别补充：

- 中断是程序切换，需要保护和恢复现场；DMA 除预处理和后处理外不占用 CPU。
- 中断传送靠 CPU 执行程序，DMA 传送靠硬件。
- DMA 请求优先级通常高于普通中断请求。
- 中断能处理异常事件，DMA 主要用于成组数据传送。

DMA 传送方式常见三类：

- 停止 CPU 访问主存/成组传送：DMA 连续占用总线，速度快但 CPU 暂停明显。
- 周期挪用/周期窃取：DMA 每次占用一个或几个存储周期，CPU 被短暂停顿。
- 透明传送：DMA 利用 CPU 不访问主存的空闲周期传送，不影响 CPU，但速度较慢。

### 11. 题型核查结论

没有发现新的大题型。仍按老师给的四类准备：单选 15 道、简答 4 道、计算 4 道、应用 3 道。

但应用题要再细分成 5 种小形态：

1. 存储器扩展框图。
2. 指令格式/寻址方式/扩展操作码。
3. Cache/虚存地址字段和程序访问命中率。
4. CPU 数据通路、指令周期、微操作控制信号。
5. 流水线时空图、数据相关、阻塞原因。
"""

